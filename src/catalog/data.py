def infer_category_from_text(text: str | None) -> str | None:
    if not text:
        return None
    normalized = normalize_text(text)
    for term, category in sorted(PRODUCT_TO_CATEGORY.items(), key=lambda item: len(item[0]), reverse=True):
        if re.search(rf"(^|\b){re.escape(term)}(\b|$)", normalized):
            return category
    return None
